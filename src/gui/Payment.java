    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String subject = jComboBox1.getSelectedItem().toString();
        String month = jComboBox2.getSelectedItem().toString();
        String payment = jTextField1.getText();

        if (jLabel6.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please select student", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (subject.equals("--Select--")) {
            JOptionPane.showMessageDialog(this, "Please select subject", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (month.equals("--Select--")) {
            JOptionPane.showMessageDialog(this, "Please select month", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (payment.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please add payment", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = Connection.search("SELECT `subno` FROM `subject` WHERE `name` = '" + subject + "'");
                rs.next();

                Connection.iud("INSERT INTO `invoice`(`student_sno`,`month`,`value`,`subject_subno`) VALUES('" + this.id + "','" + month + "','" + jLabel14.getText() + "','" + rs.getString(1) + "')");

                String path = "src//reports//invoice.jasper";

                Date date = new Date();
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

                ResultSet rs1 = Connection.search("SELECT LAST_INSERT_ID()");
                rs1.next();

                HashMap parameters = new HashMap();
                parameters.put("Parameter1", rs1.getString(1));
                parameters.put("Parameter2", sdf.format(date));
                parameters.put("Parameter3", String.valueOf(this.id));
                parameters.put("Parameter4", jLabel6.getText());
                parameters.put("Parameter5", subject);
                parameters.put("Parameter6", jLabel12.getText());
                parameters.put("Parameter7", month);
                parameters.put("Parameter8", payment);
                parameters.put("Parameter9", payment);

                JREmptyDataSource dataSource = new JREmptyDataSource();

                JasperPrint print = JasperFillManager.fillReport(path, parameters, dataSource);
                JasperViewer.viewReport(print);

                clearFields();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

