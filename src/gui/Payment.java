    public void loadSubject() {
        try {

            ResultSet rs = Connection.search("SELECT `subject`.`name`,`subject`.`price`,`teacher`.`name` FROM `class_has_student` "
                    + "INNER JOIN `class` ON `class_has_student`.`class_classno` = `class`.`classno` "
                    + "INNER JOIN `subject` ON `class`.`subject_subno` = `subject`.`subno` "
                    + "INNER JOIN `teacher` ON `class`.`teacher_tno` = `teacher`.`tno` WHERE `class_has_student`.`student_sno` = '" + this.id + "'");

            Vector v = new Vector();
            v.add("--Select--");

            while (rs.next()) {
                v.add(rs.getString("subject.name"));
                price.put(rs.getString("subject.name"), rs.getString("subject.price"));
                teacher.put(rs.getString("subject.name"), rs.getString("teacher.name"));
            }

            jComboBox1.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
        }
    }

