    public void loadTeacher() {
        try {
            ResultSet rs = Connection.search("SELECT `teacher`.`tno`,`teacher`.`name`,`teacher`.`address`,`subject`.`name` FROM `teacher` INNER JOIN `subject` ON `teacher`.`subject_subno` = `subject`.`subno`");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                v.add(rs.getString(4));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadTeacher(String text) {
        try {
            ResultSet rs = Connection.search("SELECT `teacher`.`tno`,`teacher`.`name`,`teacher`.`address`,`subject`.`name` FROM `teacher` INNER JOIN `subject` ON `teacher`.`subject_subno` = `subject`.`subno`"
                    + " WHERE `teacher`.`tno` LIKE '" + text + "%' OR `teacher`.`name` LIKE '%" + text + "%'");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                v.add(rs.getString(4));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

