    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String address = jTextArea1.getText();
        String subject = jComboBox1.getSelectedItem().toString();

        if (btn != 1) {
            if (name.isBlank()) {
                JOptionPane.showMessageDialog(this, "Please add name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (address.isBlank()) {
                JOptionPane.showMessageDialog(this, "Please add address", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (subject.equals("--Select--")) {
                JOptionPane.showMessageDialog(this, "Please select subject", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                try {
                    ResultSet rs = Connection.search("SELECT `subno` FROM `subject` WHERE `name` = '" + subject + "'");
                    rs.next();

                    Connection.iud("INSERT INTO `teacher`(`name`,`address`,`subject_subno`) VALUES('" + name + "','" + address + "','" + rs.getString(1) + "')");

                    clearFields();
                    loadTeacher();

                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        } else {
            try {
                ResultSet rs = Connection.search("SELECT `subno` FROM `subject` WHERE `name` = '" + subject + "'");
                rs.next();

                Connection.iud("UPDATE `teacher` SET `name` = '" + name + "',`address` = '" + address + "',`subject_subno` = '" + rs.getString(1) + "' WHERE `name` = '" + jTable1.getValueAt(jTable1.getSelectedRow(), 1) + "'");
                clearFields();
                loadTeacher();
                jButton1.setText("Add Teacher");
                this.btn = 0;
                
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

