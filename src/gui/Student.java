    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int confirm = JOptionPane.showConfirmDialog(this, "Do You Want Delete Student?", "Warning", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

            if (confirm == JOptionPane.YES_OPTION) {
                int row = jTable1.getSelectedRow();
                Connection.iud("DELETE FROM `student` WHERE `sno` = '" + jTable1.getValueAt(row, 0) + "'");
                loadStudent();
                JOptionPane.showMessageDialog(this, "Student Deleted Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        }

        if (evt.getClickCount() == 1) {
            if (this.c != null) {

                int x = 0;

                for (int i = 0; i < c.jTable2.getRowCount(); i++) {
                    if (c.jTable2.getValueAt(i, 0).equals(jTable1.getValueAt(jTable1.getSelectedRow(), 0))) {
                        JOptionPane.showMessageDialog(this, "Student Alredy Added", "Warning", JOptionPane.WARNING_MESSAGE);
                        x = 1;
                    }
                }

                if (x != 1) {
                    DefaultTableModel dtm = (DefaultTableModel) c.jTable2.getModel();
                    Vector v = new Vector();
                    v.add(jTable1.getValueAt(jTable1.getSelectedRow(), 0));
                    v.add(jTable1.getValueAt(jTable1.getSelectedRow(), 1));
                    dtm.addRow(v);
                    this.dispose();
                }
            }else if(this.p != null){
                p.id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                p.jLabel6.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
                p.jLabel8.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());
                p.jLabel10.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString());
                this.dispose();
            }else {
                jTextField1.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
                jTextArea1.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());

                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                try {
                    jDateChooser1.setDate(sdf.parse(jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString()));
                    jButton1.setText("Update Student");
                    btn = 1;
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

