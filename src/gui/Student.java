    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String address = jTextArea1.getText();

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String dob = sdf.format(jDateChooser1.getDate());

        if (btn != 1) {
            if (name.isBlank()) {
                JOptionPane.showMessageDialog(this, "Please add name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (address.isBlank()) {
                JOptionPane.showMessageDialog(this, "Please add address", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (jDateChooser1.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Please select date of birth", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                Connection.iud("INSERT INTO `student`(`name`,`address`,`dob`) VALUES('" + name + "','" + address + "','" + dob + "')");
                clearFields();
                loadStudent();
            }
        } else {
            Connection.iud("UPDATE `student` SET `name` = '" + name + "',`address` = '" + address + "',`dob` = '" + dob + "' "
                    + "WHERE `name` = '" + jTable1.getValueAt(jTable1.getSelectedRow(), 1) + "'");
            clearFields();
            loadStudent();
            jButton1.setText("Add Student");
            this.btn = 0;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

