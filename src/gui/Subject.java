    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int confirm = JOptionPane.showConfirmDialog(this, "Do You Want Delete Subject?", "Warning", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

            if (confirm == JOptionPane.YES_OPTION) {
                int row = jTable1.getSelectedRow();
                Connection.iud("DELETE FROM `subject` WHERE `subno` = '" + jTable1.getValueAt(row, 0) + "'");
                loadSubject();
                JOptionPane.showMessageDialog(this, "Subject Deleted Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        } else if (evt.getClickCount() == 1) {
            jTextField1.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
            jTextArea1.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());
            jTextField3.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString());
            jButton1.setText("Update Subject");
            this.btn = 1;
        }
    }//GEN-LAST:event_jTable1MouseClicked

