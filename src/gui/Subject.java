    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String description = jTextArea1.getText();
        String price = jTextField3.getText();

        if (this.btn != 1) {
            if (name.isBlank()) {
                JOptionPane.showMessageDialog(this, "Please add name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (description.isBlank()) {
                JOptionPane.showMessageDialog(this, "Please add address", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (price.isBlank()) {
                JOptionPane.showMessageDialog(this, "Please add price", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                Connection.iud("INSERT INTO `subject`(`name`,`description`,`price`) VALUES('" + name + "','" + description + "','" + Double.parseDouble(price) + "')");

                clearFields();
                loadSubject();
            }
        } else {
            Connection.iud("UPDATE `subject` SET `name` = '" + name + "',`description` = '" + description + "',`price` = '" + price + "' WHERE `name` = '" + jTable1.getValueAt(jTable1.getSelectedRow(), 1) + "' && `description` = '" + jTable1.getValueAt(jTable1.getSelectedRow(), 2) + "'");
            clearFields();
            loadSubject();
            jButton1.setText("Add Subject");
            this.btn = 0;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

